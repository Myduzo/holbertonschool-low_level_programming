#include "holberton.h"
/**
 * print_diagonal - check the code for Holberton School students.
 *
 * Return: Always 0.
 */
void print_diagonal(int n)
{
int n = 16, j = 0;
int i;
for (i = 0; i <= n; i++)
{
if (n > 0)
{
_putchar('\\');
}
{
if (j = i + 1)
_putchar('\n');
_putchar(' ');
}
_putchar('\n');
}
}
